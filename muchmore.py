def ch(c, n):
    start = ord(c)*16 + n
    res = ""
    for p in range(8):
        if start > 4095:
            res += " "
            continue
        if nn[start] & 2**(7-p):
            res += "*"
        else:
            res += " "
    return res

def line(x):
    x = x.replace("\t", "    ")
    res = ""
    for n in range(16):
        out = ""
        for c in x:
            out += ch(c, n)
        res += out + "\n"
    return res

